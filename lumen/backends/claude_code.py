"""Claude Code bridge — Collaborative mode.

In Collaborative mode, the AI assistant (Claude Code) works alongside the reader
incrementally. This bridge exports and imports session handoff notes to maintain
progress across multiple conversations.
"""

from datetime import datetime, timezone
from pathlib import Path
from typing import Any


class ClaudeCodeBridge:
    """Bridge for Collaborative mode reading sessions.

    Generates structured session handoff notes that Claude Code can read
    to resume book analysis across multiple conversations.
    """

    def __init__(self, work_dir: str = "output"):
        self.handoff_dir = Path(work_dir) / "handoff"
        self.handoff_dir.mkdir(parents=True, exist_ok=True)

    def write_handoff(
        self,
        book_slug: str,
        phase: str,
        status: str,
        notes: list[str],
        next_steps: list[str],
    ) -> str:
        """Write a session handoff note for the next conversation.

        Returns the handoff file path.
        """
        now = datetime.now(timezone.utc).isoformat()
        session_id = now.replace(":", "-")
        path = self.handoff_dir / f"{book_slug}-{session_id}.md"

        content = [
            f"# Session Handoff: {book_slug}",
            "",
            f"- **Session**: {session_id}",
            f"- **Phase**: {phase}",
            f"- **Status**: {status}",
            f"- **Timestamp**: {now}",
            "",
            "## Progress Notes",
            "",
        ]
        for note in notes:
            content.append(f"- {note}")

        content.extend(["", "## Next Steps", ""])
        for step in next_steps:
            content.append(f"- [ ] {step}")

        content.append("")
        content.append("---")
        content.append("*This handoff was generated by Lumen's Collaborative mode bridge.*")
        content.append("")

        text = "\n".join(content)
        path.write_text(text, encoding="utf-8")
        return str(path)

    def load_handoff(self, book_slug: str) -> list[dict[str, Any]]:
        """Load all handoff notes for a book, sorted newest-first."""
        handoffs = []
        for f in sorted(self.handoff_dir.glob(f"{book_slug}-*.md"), reverse=True):
            text = f.read_text(encoding="utf-8")
            handoffs.append({
                "path": str(f),
                "content": text,
                "filename": f.name,
            })
        return handoffs

    def get_latest_context(self, book_slug: str) -> dict[str, Any]:
        """Get the latest phase and notes from handoff history."""
        handoffs = self.load_handoff(book_slug)
        if not handoffs:
            return {"phase": "init", "notes": [], "next_steps": []}

        latest = handoffs[0]
        content = latest["content"]
        # Extract phase
        phase = "unknown"
        for line in content.split("\n"):
            if line.startswith("- **Phase**:"):
                phase = line.split(":", 1)[-1].strip()
                break

        # Extract notes
        notes = []
        in_notes = False
        for line in content.split("\n"):
            if line.strip().startswith("## Progress Notes"):
                in_notes = True
                continue
            if line.strip().startswith("## Next Steps"):
                in_notes = False
                continue
            if in_notes and line.strip().startswith("- "):
                notes.append(line.strip()[2:])

        return {"phase": phase, "notes": notes}
